package Elements;

public class Element {
	char estado = ' ';
	public int x;
	public int y;

	public char getEstado()
	{
		return estado;
	}

	
	public int getX()
	{
		return x;
	}
	
	public int getY()
	{
		return y;
	}
	
	public void setX(int x)
	{
		this.x = x;
	}
	
	public void setY(int y)
	{
		this.y = y;
	}

}
